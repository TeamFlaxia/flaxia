import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const sdkPath = join(root, 'packages/multiplayer/dist/multiplayer-sdk.iife.js');
const outputPath = join(root, 'src/lib/multiplayer-sdk.generated.ts');

try {
  const sdkContent = readFileSync(sdkPath, 'utf-8');
  const escaped = sdkContent.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$');

  const ts = `// Auto-generated by scripts/embed-multiplayer-sdk.mjs — do not edit manually
// biome-ignore lint/suspicious/noUselessEscapeInString: auto-generated IIFE bundle
export const MULTIPLAYER_SDK_IIFE: string = \`${escaped}\`;
`;

  writeFileSync(outputPath, ts, 'utf-8');
  console.log(`Generated ${outputPath} (${sdkContent.length} bytes)`);
} catch (err) {
  console.error('Failed to embed multiplayer SDK:', err);
  process.exit(1);
}
