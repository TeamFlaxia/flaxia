import { execSync } from 'node:child_process';
import { copyFileSync, existsSync, mkdirSync, readFileSync, unlinkSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

// launcher icons (generate from assets/icon.png via capacitor-assets)
try {
  execSync('npx capacitor-assets generate --android --assetPath assets', {
    stdio: 'pipe',
    timeout: 30000,
  });
  console.log('Generated Android launcher icons');
} catch (err) {
  console.error('WARNING: Failed to generate launcher icons from assets/icon.png.');
  console.error('App will use default Capacitor icons. Ensure @capacitor/assets and its');
  console.error('dependencies (sharp) are properly installed.');
  console.error(`Error: ${err.stderr?.toString().trim() || err.message}`);
}

// Remove the emoji-face vector generated by cap add android (drawable-v24)
// which overrides the proper Flaxia launcher foreground from assets/icon.png.
const faceIconPath = resolve('android/app/src/main/res/drawable-v24/ic_launcher_foreground.xml');
if (existsSync(faceIconPath)) {
  unlinkSync(faceIconPath);
  console.log('Removed drawable-v24/ic_launcher_foreground.xml (emoji face)');
}

// Copy the generated mipmap foreground PNG to drawable/ so it can be
// referenced as R.drawable.ic_launcher_foreground for the notification
// smallIcon. The mipmap PNGs are generated from assets/icon.png above.
const drawableDir = resolve('android/app/src/main/res/drawable');
const foregroundPng = resolve(drawableDir, 'ic_launcher_foreground.png');
if (!existsSync(foregroundPng)) {
  mkdirSync(drawableDir, { recursive: true });
  const mipmapHdpi = resolve('android/app/src/main/res/mipmap-hdpi/ic_launcher_foreground.png');
  if (existsSync(mipmapHdpi)) {
    copyFileSync(mipmapHdpi, foregroundPng);
    console.log('Copied ic_launcher_foreground.png to drawable/');
  }
}

const buildGradlePath = resolve('android/app/build.gradle');
if (!existsSync(buildGradlePath)) {
  console.log('build.gradle not found — skipping patches');
  process.exit(0);
}

let content = readFileSync(buildGradlePath, 'utf8');

// APK output filename
if (!content.includes('flaxia_install')) {
  content = content.replace(
    /(android\s*\{)/,
    `$1
    applicationVariants.all { variant ->
        variant.outputs.all {
            outputFileName = "flaxia_install.apk"
        }
    }`,
  );
  console.log('Patched APK output filename');
}

const keystoreFile = process.env.ANDROID_KEYSTORE_FILE;
const keystorePassword = process.env.ANDROID_KEYSTORE_PASSWORD;
const keyAlias = process.env.ANDROID_KEY_ALIAS;
const keyPassword = process.env.ANDROID_KEY_PASSWORD;

if (keystoreFile && keystorePassword && keyAlias && keyPassword) {
  content = content.replace(
    /android\s*\{/,
    `android {
    signingConfigs {
        release {
            storeFile file('${keystoreFile.replace(/'/g, "\\'")}')
            storePassword '${keystorePassword.replace(/'/g, "\\'")}'
            keyAlias '${keyAlias.replace(/'/g, "\\'")}'
            keyPassword '${keyPassword.replace(/'/g, "\\'")}'
        }
    }`,
  );

  content = content.replace(
    /(buildTypes\s*\{[^}]*?release\s*\{)/,
    '$1\n            signingConfig signingConfigs.release',
  );
  console.log('Signing config patched into build.gradle');
} else {
  console.log('Missing signing env vars — skipping signing patch');
}

writeFileSync(buildGradlePath, content);
